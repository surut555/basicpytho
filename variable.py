a = 3
b = 4.92


print(a)
print(b)

print(a, b)

# การสร้างตัวแปรหลายตัวในบรรทัดเดียวกัน

# ตัวแสดงผลร่วมกับข้อความ
# วิธีที่ 1 concat string
print("ราคาขายต่อชิ้น", b, "บาท มีจำนวน", a, "ขิ้น")

# วิธีที่ 2 string interpolation
print("ราคาขายต่อชิ้น %.2fบาท มีจำนวน %d ขิ้น" % (b, a))

# วิธีที่3 format string
print(f"ราคาขายต่อชิ้น {b}บาท มีจำนวน {a} ชิ้น")
